package methods;

public class ArithmeticOperationAddition {
     
	int addition( int a, int b) {
		int sum = a + b ;
		return sum;
	}
	
	int addition(int x, int y, int z) {
		int sum = x + y + z;
		return sum;
	}
	
	double addition(double p, double q){
		double sum = p + q;
		return sum;
	}
}
